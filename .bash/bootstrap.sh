#!/bin/sh
#
# ~/.bash/bootstrap.sh
#

##################################################################################
# Init PATH and aliases before anything else
##################################################################################
[ -f "$HOME/.bash/path.sh" ] && source "$HOME/.bash/path.sh"
[ -f "$HOME/.bash/alias.sh" ] && source "$HOME/.bash/alias.sh"

##################################################################################
# Exit NOW if not running interactively
##################################################################################
[[ $- != *i* ]] && return

##################################################################################
# Init Shell Utils
##################################################################################
[ -f "$HOME/.bash/shell-utils.sh" ] && source "$HOME/.bash/shell-utils.sh"

##################################################################################
# Bash Prompt
##################################################################################
PS1='[\u@\h \W]\$ '
_PS1='svjson'
source ~/.bash/prompt.sh

##################################################################################
# ssh-agent auth socket
##################################################################################
source ~/.bash/ssh.sh

##################################################################################
# Global reverse-i-search with fzf
##################################################################################
source ~/.bash/command-history.sh

##################################################################################
# Use gitprofiles script
##################################################################################
source ~/.bash/git.sh

##################################################################################
# Pimp my cd, and global dir history search with fzf
##################################################################################
source ~/.bash/pwd-history.sh
